def soma(n1,n2):
    r = n1 + n2
    print(f"A soma de {n1} + {n2} é {r}")
    return r

def subtrair(n1,n2):
    r = n1 - n2
    print(f"A subtração de {n1} - {n2} é {r}")
    return r

def multiplicar(n1,n2):
    r = n1 * n2 
    print(f"A multiplicação de {n1} x {n2} é {r}")
    return r

def divisao(n1,n2):
    r = n1 / n2 
    print(f"A multiplicação de {n1} / {n2} é {r}")
    return r

print(soma(3,6))

print(subtrair(3,6))

print(multiplicar(3,6))

print(divisao(3,6))